## 第三条 推广行为规范

**3.1 合规推广要求**

甲方仅可使用乙方提供、审核通过或允许使用的推广素材、推广链接、二维码、海报、页面截图或说明文案进行推广，不得擅自制作、篡改、拼接、夸大、断章取义或以其他方式形成误导性宣传内容。

**3.2 禁止金融化宣传**

甲方在推广过程中，严禁以任何形式向第三方作出下列宣传、承诺或暗示：

1. 保本、保息、保收益、稳赚不赔、年化回报、静态分红、躺赚、复利等金融化表述；
2. 将三水元设备购买包装为投资项目、理财产品、基金份额、合伙入股或其他金融产品；
3. 承诺被推荐人加入后一定获利、一定回本、一定产生高额奖励或稳定收入；
4. 夸大推荐关系链收益、制造“拉人头返现”“团队计酬”“多级分销致富”等误导性预期。

**3.3 禁止诱导与强制分享**

甲方不得通过以下方式开展推广：

1. 以强制分享、任务解锁、胁迫转发、虚假抽奖等方式诱导用户传播；
2. 以刷单、虚假注册、批量拉新、模拟设备、脚本调用、作弊裂变等方式制造虚假推广效果；
3. 以夸大赠品、虚假福利、虚构优惠、虚构设备功能或隐瞒重要交易条件等方式误导被推荐人；
4. 以朋友圈层级展示、下线排行、团队收益榜等具有传销或多级团队视觉特征的方式进行宣传。

**3.4 分享边界**

甲方知悉并同意，乙方平台当前推广关系仅用于合规的用户介绍和一次性实物销售介绍费确认。乙方有权根据平台能力、微信生态规范、风控要求及监管政策，对分享路径、展示形式、分享文案、推广页内容和推广能力进行限制、调整、中止或关闭。

