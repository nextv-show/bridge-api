## 第四条 推荐关系确认规则

**4.1 首次有效绑定原则**

被推荐人与甲方之间的推荐关系，原则上仅在被推荐人首次通过合规推广入口进入平台并完成乙方认可的有效绑定流程时成立；同一被推荐人的推荐关系一经有效绑定，原则上不得任意变更。

**4.2 两级封顶原则**

乙方平台推荐关系最多记录至 L2：

1. 甲方可因其直接推荐的被推荐人形成 L1 关系；
