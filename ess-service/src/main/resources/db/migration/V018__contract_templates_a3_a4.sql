## 第五条 一次性实物销售介绍费的冻结、确认、取消与结算

**5.1 报酬定性**

双方一致确认，甲方基于本协议可能取得的报酬，仅定性为**一次性实物销售介绍费**。该报酬与机主本人基于设备真实用水消费取得的“运营服务分成”属于不同法律性质的款项，两者不得混同。

**5.2 触发条件**

甲方取得一次性实物销售介绍费，应同时满足以下条件：

1. 被推荐人与甲方之间已形成符合本协议约定的有效推荐关系；
2. 被推荐人完成乙方认可的首个合格购机订单支付；
3. 该订单在平台风控、实名、签约、支付、退款、争议和合规校验中未被认定为异常；
4. 其他乙方公示的必要条件已经满足。

**5.3 固定金额规则**

一次性实物销售介绍费按乙方届时有效的平台公示规则及系统固化标准执行，并适用以下约束：

1. L1 和 L2 可对应不同固定金额；
2. 固定金额可按机型、活动或阶段配置不同档位；
3. 同一笔介绍费金额以**被推荐人对应首个合格订单支付成功时的系统快照金额**为准，后续规则调整不追溯影响已冻结记录；
4. 任何情况下，介绍费均不得以购机款百分比、投资收益率、团队规模比例、层级分账公式或其他浮动比例方式计算。

**5.4 公示规则优先**

对于本协议未逐项展开约定的固定金额标准、活动口径、冷静期时长、展示方式、结算周期、提现门槛、提现路径、税务处理流程、风控审核规则等事项，双方同意以乙方届时有效的平台公示规则为准；但平台公示规则不得违反本协议关于“两级封顶、固定金额、每人一次、去传销、去金融化”的核心约束。

**5.5 状态定义**

一次性实物销售介绍费采用以下状态管理：

1. **FROZEN（冻结待确认）**：被推荐人首个合格订单支付成功后先进入该状态；
2. **CONFIRMED（已确认）**：被推荐人订单对应冷静期届满且未发生取消情形后转入该状态；
3. **CANCELLED（已取消）**：因退款、撤销、争议、风控异常、身份异常、订单失效或其他不符合规则情形而转入该状态。

上述状态按平台规则和系统逻辑单向流转。除乙方确认存在系统错误、数据错误或法律法规另有要求外，已取消状态原则上不恢复。

**5.6 冷静期冻结规则**

被推荐人订单支付成功后，对应的一次性实物销售介绍费先进入 **FROZEN（冻结待确认）** 状态。在该状态下：

1. 乙方有权仅展示“冻结中”“待确认”等状态，而不向甲方展示具体可结算金额；
2. FROZEN 状态下的介绍费不代表乙方已形成最终付款义务；
3. 冻结期间如发生退款、撤销、争议、风控异常、身份异常或其他不符合确认条件的情形，乙方有权直接将该笔介绍费转为 **CANCELLED（已取消）**。

**5.7 确认规则**

冻结期届满且对应订单持续有效、未发生取消情形的，乙方可按平台规则将该笔介绍费转为 **CONFIRMED（已确认）**。仅在 CONFIRMED 状态下，该笔介绍费方可计入甲方可确认总额、可见金额或后续结算范围。

**5.8 取消规则**

如被推荐人的相关订单发生冷静期内退款、冷静期后退款、合同撤销、支付撤销、订单关闭、司法冻结、风控撤销或其他导致原交易基础不再成立的情形，乙方有权取消与该被推荐人关联的介绍费记录：

1. 冷静期内退款或撤销的，乙方可将 **FROZEN → CANCELLED**；
2. 冷静期届满后退款或原交易被解除的，乙方可将 **CONFIRMED → CANCELLED**；
3. 如相关款项已实际结算，乙方有权依法追回、抵扣或在后续应付款中冲减。

**5.9 结算与提现**

CONFIRMED 状态的介绍费，可依据乙方届时有效的平台公示规则进入结算或提现流程。具体结算周期、结算条件、最低提现门槛、提现路径、到账时效、手续要求及必要风控验证，以乙方届时有效的平台公示规则为准。

**5.10 税务代扣与合规处理**

甲方基于本协议取得的一次性实物销售介绍费涉及的税费、票据、信息申报、代扣代缴或其他合规处理事项，由双方依法律法规及平台规则各自承担相应责任；法律法规要求乙方或乙方合作平台代扣代缴的，甲方同意乙方依法办理。甲方应按乙方要求提供真实、完整、有效的税务处理所需资料；因甲方拒绝提供、延迟提供或提供错误资料导致结算延迟、代扣失败、补税风险或其他损失的，由甲方自行承担相应责任。

