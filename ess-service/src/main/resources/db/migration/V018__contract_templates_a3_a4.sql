3. 任何 L3 及以上层级的关系，不作为甲方取得介绍费的依据，也不纳入本协议项下的报酬统计、确认或展示范围。

**4.3 每个被推荐人仅一次原则**

无论被推荐人后续下单次数、设备数量、消费次数、分享次数或其他行为如何变化，甲方基于同一被推荐人仅可按本协议及平台规则获得一次对应层级的一次性实物销售介绍费，不得重复累计、复利生成或按订单持续获取。

**4.4 关系绑定证明**

双方确认，系统留痕、推广链接参数、推广码、访问记录、登录绑定记录、订单快照、风控校验记录及其他平台数据，可作为推荐关系确认依据。乙方对推荐关系及层级关系享有最终系统校验权和风控判断权。

