24小时冷静期届满且甲方未行使撤销权的，本协议正式生效，设备正式进入物联网激活、物流派发或场地撮合阶段。若甲方此后申请退款，将适用以下规则：

6.3.1 **未进行物流发货及物理安装**：设备尚未出库或尚未与终端需求方完成撮合的，甲方可申请退单。但因相关协议已生效且触发了系统撮合与运维成本，平台将扣除购机款总额的 **5%** 作为违约金及运营补偿，余款予以退还。

6.3.2 **已完成物流配送或安装激活**：鉴于智能水机属于涉及饮用水安全的特殊设备，一旦涉水激活，其物理属性及卫生标准发生根本改变。此阶段原则上不支持全额退单。甲方如确需退出，设备需按平台"二手设备残值评估标准"进行折价回收，或由甲方自行提取设备物理实体退出代运营网络。

**6.4 严禁回购与去金融化申明**

6.4.1 本条所指"退款"仅针对实体商品买卖合同的解除。平台严禁以"理财产品赎回""保本付息到期退出"等名义进行任何形式的资金本息兜底退还。

