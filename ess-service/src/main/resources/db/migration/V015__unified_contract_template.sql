## **第二条 代运营服务内容与平台职责**

**2.1 供需撮合**

乙方通过系统核心撮合引擎，将甲方的闲置设备资源精准推送给有真实饮水需求的B端场所或C端家庭（即"终端用水用户"），并在达成撮合后调度完成物流与上门安装。

**2.2 技术监测与存证**

