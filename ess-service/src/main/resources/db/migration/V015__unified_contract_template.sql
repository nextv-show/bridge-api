## **第六条 冷静期与退款规则**

**6.1 设立目的与合规声明**

为保障消费者及购机用户的合法权益，防范冲动消费，并严格遵循国家关于规范商业行为、防范非法吸收公众存款等相关法律法规，三水元平台特设立24小时交易冷静期及明确的退款规则。

**6.2 24小时冷静期规则**

