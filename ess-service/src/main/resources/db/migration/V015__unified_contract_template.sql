## **第三条 服务返利与结算标准**

**3.1 消费驱动结算**

甲方的服务返利完全挂钩于终端用户的真实用水消费（单价设定按终端实际撮合为准，如0.4元至1.5元/升）及客观的IoT物联网数据。平台按照《平台自动分账规则》，在扣除对应的耗材、维护及运营成本后，将运营分成以系统自动清分形式结算至甲方账户。

**3.2 断网缓存补偿**

若设备处于弱网或断网环境，主板支持最高30天数据离线缓存补录，设备恢复联网后系统将自动校验并执行补单结算，确保数据不丢失。

**3.3 提现合规规则**

为保障生态健康运转并符合相关税务及运营规范，收益提现强制执行"现金+积分（健康水滴，比例为70%现金+30%积分）"拆分机制，并按系统公示比例收取必要的手续费用。

